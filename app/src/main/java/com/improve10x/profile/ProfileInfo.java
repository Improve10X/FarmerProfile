package com.improve10x.profile;

public class ProfileInfo {

    public String name;
    public String place;
    public String dob;
    public String preferredCrop;

    public ProfileInfo() {
    }

    public ProfileInfo(String name, String place, String dob, String preferredCrop) {
        this.name = name;
        this.place = place;
        this.dob = dob;
        this.preferredCrop = preferredCrop;
    }
}
